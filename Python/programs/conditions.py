# if condition
x = 55
if x < 45 :
    print("it is in if block")
    print("x is less than 45")
else :
    print("it is in outof if block ")
    print("x is greater than 45")

# if/elif /else conditions
x = 25
if x < 42:
    print("x is less than 42")
elif x == 42:
    print("x is equal to 42")
else:
    print("x is greater than 42")